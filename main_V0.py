class Champs :
    def __init__ (self, x, y) :
        self.x = x
        self.y = y
        self.width = 20
        self.height = 20
        self.color = (0, 86, 27)
        self.infla = True
        self.etat = "eteint"
        self.temps_combu = random.randint(300, 600)
        self.temps_brule = 0
        self.adjacents = []
    
    def main(self, display) :
        
        self.adjacents = get_adj(self.x, self.y)
            
        if self.etat == "brule" :
            self.temps_brule += 1
            if self.temps_combu == self.temps_brule :
                self.etat = "consume"
                self.infla = False
            for i in range (len(self.adjacents)) :
                if self.adjacents[i].infla :
                    if random.randint(1,500) == 42 :
                        self.adjacents[i].etat = "brule"
        
        
        if self.etat == "eteint" :
            self.color = (0, 86, 27)
        if self.etat == "brule" :
            self.color = (187, 11, 11)
        if self.etat == "consume" :
            self.color = (70, 63, 50)
        
        pygame.draw.rect(display, self.color, (self.x, self.y, self.width, self.height))
        
