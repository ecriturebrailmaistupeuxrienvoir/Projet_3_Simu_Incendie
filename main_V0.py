class Route :
    def __init__ (self, x, y) :
        self.x = x
        self.y = y
        self.width = 20
        self.height = 20
        self.color = (103, 113, 121)
        self.infla = False

    def main(self, display) :
        pygame.draw.rect(display, self.color, (self.x, self.y, self.width, self.height))

class Champs :
    def __init__ (self, x, y) :
        self.x = x
        self.y = y
        self.width = 20
        self.height = 20
        self.color = (0, 86, 27)
        self.infla = True
        self.etat = "eteint"
        self.adjacents = []
    
    def main(self, display) :
        
        self.adjacents = [get_adj(self.x, self.y)]
        
        if self.etat == "brule" :
            for i in self.adjacents :
                if self.adjacents[i].infla :
                    if random.randint(1,300) == 0 :
                        self.adjacents[i].etat = "brule"

        if self.etat == "eteint" :
            self.color = (0, 86, 27)
        if self.etat == "brule" :
            self.color = (187, 11, 11)
        if self.etat == "consume" :
            self.color = (70, 63, 50)
        
        pygame.draw.rect(display, self.color, (self.x, self.y, self.width, self.height))
        
